# Dictionaries in Python
# What is Dictionaries in python?
# A dictionary is a collection of key-value pairs.  
# It can be thought of as an array, but instead of using indices to access elements,  we use keys to access values. 
# It can be defined using curly brackets {} or by the type() function.  

car = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964,
}
print(car["model"])